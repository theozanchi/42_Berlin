int	ft_iterative_power(int nb, int power)
{
	int	result;

	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else
	{
		result = nb;
		while (power-- > 1)
			result = result * nb;
		return (result);
	}
}
